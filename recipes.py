def calculate_recipe_cost(recipe_id: str, store: str) -> int:
    shares = RECIPE_INGREDIENT_SHARES.get(recipe_id, {})
    prices = STORE_PRICES.get(store.lower(), STORE_PRICES["пятерочка"])
    total_cost = 0
    for ingredient, share in shares.items():
        # Если ингредиента нет в базе цен конкретной сети, берется базовая заглушка
        price = prices.get(ingredient, 100)
        total_cost += price * share
    return int(total_cost)